#include <stdio.h>

/**
 * main - The last digit
 * @void: Empty parameter list for main.
 *
 * Description: Prints last digit of random number and tells
 * if it is greater than 5, 0, or less than 6 and not 0.
 *
 * Return: 0 for success
*/
int main(void)
{
	char moses;


	for (moses = 'a'; moses < 'z'; moses++);
	{
		putchar(moses);
	}
	putchar("\n");
	return (0);
}
